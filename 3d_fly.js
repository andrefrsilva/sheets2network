  checkbox.addEventListener('change', event => {
    // get the group associated with the checkbox
    const group = event.target.name;

    // get all the nodes that belong to this group
    const nodes = Graph.graphData().nodes.filter(node => node.group === group);

    // get all the links associated with the nodes
    const links = Graph.graphData().links.filter(link => nodes.includes(link.source) || nodes.includes(link.target));

    // check if the group is now on
    const groupIsOn = event.target.checked;

    // if the group is now on
    if (groupIsOn) {
      // add the nodes back
      Graph.graphData().nodes.push(...nodes);

      // add the links back if both nodes belong to groups that are currently on
      const linksToAdd = links.filter(link => {
        const sourceIsOn = checkboxes[link.source.group].checked;
        const targetIsOn = checkboxes[link.target.group].checked;
        return sourceIsOn && targetIsOn;
      });
      Graph.graphData().links.push(...linksToAdd);
    } else {
      // remove the links associated with the nodes to remove
      Graph.graphData().links = Graph.graphData().links.filter(link => {
        return !nodes.includes(link.source) && !nodes.includes(link.target);
      });

      // remove the nodes
      Graph.graphData().nodes = Graph.graphData().nodes.filter(node => !nodes.includes(node));
    }

    // update the graph
    Graph.refresh();
    
  });
